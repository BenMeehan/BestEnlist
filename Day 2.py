text="DON'T TROUBLE TROUBLE UNTIL TROUBLE TROUBLES YOU."

#Capitalize
x=text.capitalize()          #first character to uppercase
print(x)

#Find
x=text.find("TROUBLE")        #first occurence index
print(x)

#isalpha
x=text.isalpha()              #true is all characters are alphabets
print(x)

#isalnum
x=text.isalnum()              #true is all characters are alphabets or numbers
print(x)
